#!/bin/bash

[ 1 -eq 1 ] # 1 等于 1
[ 1 -eq 2 ] # 1 不等于 2
[ 1 -ne 2 ] # 1 不等于 2
[ 1 -gt 2 ] # 1 大于 2
[ 1 -lt 2 ] # 1 小于 2
[ 1 -ge 2 ] # 1 大于等于 2
[ 1 -le 2 ] # 1 小于等于 2

[ "a" == "a" ] # "a" 等于 "a"
[ "a" != "b" ] # "a" 不等于 "b"
[[ "a" < "b" ]] # "a" 小于 "b"
[[ "a" > "b" ]] # "a" 大于 "b"

[ -z "" ] # 字符串 "a" 长度为 0
[ -n "a" ] # 字符串 "a" 长度不为 0
[ -f "a" ] # 文件 a 存在
[ -d "a" ] # 目录 a 存在
[ -x "a" ] # 文件 a 可执行
[ -r "a" ] # 文件 a 可读
[ -w "a" ] # 文件 a 可写
[ -s "a" ] # 文件 a 存在且非空
[ -e "a" ] # 文件 a 存在

